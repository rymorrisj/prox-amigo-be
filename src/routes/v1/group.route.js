const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const groupValidation = require('../../validations/group.validation');
const groupController = require('../../controllers/group.controller');

const router = express.Router();

router
  .route('/:userId')
  .post(auth('manageGroup'), validate(groupValidation.createGroup), groupController.createGroup)
  
router
  .route('/:groupId')
  .get(auth('getGroup'), validate(groupValidation.getGroup), groupController.getGroup)
  .patch(auth('manageGroup'), validate(groupValidation.updateGroup), groupController.updateGroup)
  .delete(auth('manageGroup'), validate(groupValidation.deleteGroup), groupController.deleteGroup);

module.exports = router;

